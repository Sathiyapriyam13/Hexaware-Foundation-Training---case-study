class Officer:
    def __init__(self, officer_id, first_name, last_name, dob, gender, badge, rank, address, contact, agency_id):
        self.officer_id = officer_id
        self.first_name = first_name
        self.last_name = last_name
        self.dob = dob
        self.gender = gender
        self.badge = badge
        self.rank = rank
        self.address = address
        self.contact = contact
        self.agency_id = agency_id
